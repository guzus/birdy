package tui

// Key bindings reference:
//
// Global:
//   ctrl+c    Quit the application
//
// Splash screen:
//   any key   Skip to chat
//
// Chat screen:
//   enter     Send message
//   tab       Switch to accounts
//   up/down   Scroll viewport
//
// Account list:
//   j/down    Move cursor down
//   k/up      Move cursor up
//   a         Add new account
//   d         Delete selected account
//   tab/esc   Return to chat
//
// Account add form:
//   tab       Next field
//   shift+tab Previous field
//   enter     Save account
//   esc       Cancel
